    public CadastrarEstoque(int idAlteracao) {
        initComponents();
        this.idAlteracao = idAlteracao;
        try{
            Connection con = DriverManager.getConnection("jdbc:postgresql://localhost:5432/cadastros","postgres","123456");
            String sql = "select * from estoque where id_estoque = ? ";
            PreparedStatement stmt = con.prepareStatement(sql);
            stmt.setInt(1, idAlteracao);
            ResultSet rs = stmt.executeQuery();
            while(rs.next()){
                txtID.setText(rs.getString("id_estoque"));
                txtID.setEnabled(false);
                txtQuan.setText(rs.getString("quantidade"));
                txtQuanM.setText(rs.getString("quantidade_minima"));
                txtDtEn.setText(rs.getString("data_entrada"));
                txtDtSa.setText(rs.getString("data_saida"));
                txtPro.setText(rs.getString("produto"));
            }
            stmt.close();
            con.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

